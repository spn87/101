<h2>Fade Gallery <span style="font-size: 8pt;">v 1.3.3</span></h2>
<p>Plugin,  Module and Component</p>
<p>Very light, safe, Javascript Fade gallery, that works  on IE, Mozilla, Chrome, Opera etc. You can embed this extension with  ease.</p>
<p>Useful to make your site look more dynamic.</p>

<p>Unlike some other galleries, there is no need to  create menu items or any other elements, just type the name of the  folder containing images and that's it, it will show up all images from  this folder, one by one in alphabetical order. Also you may type file names  separated with comma in <em>filelist</em> argument, to show exact images of selected folder and in  exact order.</p>

<p>Slide show Interval and fade effect can be adjasted  with three parameters: interval, fade time and&nbsp; fade step in  miliseconds.</p>
<p>Direct link to existing gallery example: <strong>{</strong><strong>fadegalleryid=1}</strong></p>

<p>Also you may pass parameters as shown below(no need to create gallery though recomended) <br><strong>{</strong><strong>fadegallery=<em>parameters</em>}</strong></p>
<p>parameters:  folder,width,height,interval,fadetime,fadestep,filelist,align,padding</p>
<p>example:</p>
<p><strong>{</strong><strong>fadegallery=images/galleryexample,400,200,6000,2000,10,,right,10}</strong></p>
<p>and one more: <strong>{</strong><strong>fadegallery=images/fadegallery3,250,114,6000,2000,10,front2.jpg;front1.jpg,right,10}</strong></p>



